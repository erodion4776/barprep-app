@app.post("/api/ingest/url")
def ingest_url(data: IngestRequest):
    """Scrapes a webpage and ingests into Supabase"""
    url = data.url
    try:
        res = requests.get(
            url,
            headers={"User-Agent": "Mozilla/5.0"},
            timeout=10,
            stream=True
        )
        if res.status_code != 200:
            raise HTTPException(
                status_code=400,
                detail=f"Page returned status {res.status_code}"
            )
        content = b""
        for chunk in res.iter_content(chunk_size=8192):
            content += chunk
            if len(content) > MAX_CONTENT_BYTES:
                raise HTTPException(
                    status_code=400,
                    detail=f"Page too large (max {MAX_CONTENT_BYTES // (1024*1024)}MB)"
                )
        soup = BeautifulSoup(content, "html.parser")
    except HTTPException:
        raise
    except Exception as e:
        raise HTTPException(status_code=400, detail=f"Could not reach URL: {e}")

    for tag in soup(["script", "style"]):
        tag.extract()

    title = (
        soup.title.string.strip()[:200]
        if soup.title and soup.title.string
        else "Untitled Page"
    )

    clean_text = "\n".join(
        line.strip() for line in soup.get_text().splitlines() if line.strip()
    )

    chunks = split_text(clean_text)
    if not chunks:
        return {"message": "Page contained no readable text."}

    if len(chunks) > MAX_CHUNKS:
        chunks = chunks[:MAX_CHUNKS]
        logger.warning(f"Truncated to {MAX_CHUNKS} chunks")

    try:
        embeddings = get_embeddings_batch(chunks)
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Embedding failed: {e}")

    rows = [{
        "content":  chunk,
        "metadata": {
            "source":      url,
            "title":       title,
            "type":        "webpage",
            "chunk_index": i
        },
        "embedding": embeddings[i]
    } for i, chunk in enumerate(chunks)]

    try:
        supabase.table("documents").insert(rows).execute()
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"DB insert failed: {e}")

    logger.info(f"Ingested {len(chunks)} chunks from '{title}'")
    return {
        "message": f"Successfully ingested {len(chunks)} chunks from '{title}'"
    }


@app.post("/api/ingest/youtube")
def ingest_youtube(data: IngestRequest):
    """Transcribes YouTube video and ingests into Supabase"""
    match = re.search(r"(?:v=|\/)([a-zA-Z0-9_-]{11})", data.url)
    if not match:
        raise HTTPException(status_code=400, detail="Invalid YouTube URL.")
    video_id = match.group(1)

    transcript_list = None
    last_error      = None

    for attempt in range(3):
        try:
            transcript_list = YouTubeTranscriptApi.get_transcript(video_id)
            break
        except TranscriptsDisabled:
            raise HTTPException(
                status_code=400,
                detail="Transcripts disabled for this video."
            )
        except NoTranscriptFound:
            raise HTTPException(
                status_code=404,
                detail="No transcript found for this video."
            )
        except Exception as e:
            last_error = e
            error_str  = str(e).lower()
            if "429" in str(e) or "too many requests" in error_str:
                wait_time = (attempt + 1) * 5
                logger.warning(f"YouTube rate limit. Waiting {wait_time}s...")
                time.sleep(wait_time)
                continue
            if HAS_VIDEO_UNAVAILABLE and isinstance(e, VideoUnavailable):
                raise HTTPException(
                    status_code=404,
                    detail="Video unavailable or private."
                )
            if any(k in error_str for k in ["unavailable", "private"]):
                raise HTTPException(
                    status_code=404,
                    detail="Video unavailable or private."
                )
            raise HTTPException(
                status_code=500,
                detail=f"Transcript error: {e}"
            )

    if transcript_list is None:
        raise HTTPException(
            status_code=429,
            detail="YouTube rate limiting. Please wait 5 minutes and retry."
        )

    full_transcript = " ".join(e["text"] for e in transcript_list)

    try:
        supabase.table("youtube_videos").upsert({
            "video_id":   video_id,
            "title":      f"YouTube Lecture: {video_id}",
            "transcript": full_transcript
        }).execute()
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"DB upsert failed: {e}")

    chunks = split_text(full_transcript)
    if not chunks:
        return {"message": f"Video {video_id} had no usable transcript text."}

    if len(chunks) > MAX_CHUNKS:
        chunks = chunks[:MAX_CHUNKS]

    try:
        embeddings = get_embeddings_batch(chunks)
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Embedding failed: {e}")

    rows = [{
        "content":  chunk,
        "metadata": {
            "source":      f"https://youtube.com/watch?v={video_id}",
            "video_id":    video_id,
            "type":        "youtube_transcript",
            "chunk_index": i
        },
        "embedding": embeddings[i]
    } for i, chunk in enumerate(chunks)]

    try:
        supabase.table("documents").insert(rows).execute()
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"DB insert failed: {e}")

    logger.info(f"Processed video {video_id} into {len(chunks)} chunks")
    return {
        "message": f"Processed video {video_id} into {len(chunks)} chunks."
    }


@app.post("/api/process-video")
def process_video(data: VideoRequest):
    """
    Full video processing pipeline:
    1. Get metadata via YouTube Data API v3
    2. Try transcript with 10s thread timeout
    3. Fallback to description if blocked
    4. Generate AI course content via Groq
    5. Save module to Supabase
    6. Embed for RAG search
    """
    match = re.search(r"(?:v=|\/)([a-zA-Z0-9_-]{11})", data.url)
    if not match:
        raise HTTPException(
            status_code=400,
            detail="Invalid YouTube URL"
        )

    video_id = match.group(1)
    logger.info(f"=== Processing {video_id} | {data.topic} ===")

    # Step 1: Get metadata via YouTube Data API v3
    meta = get_video_metadata(video_id)
    logger.info(f"Title: {meta['title']}")

    # Step 2: Try transcript with thread timeout
    logger.info("Attempting transcript (10s timeout)...")
    transcript = get_transcript_with_timeout(video_id, timeout_seconds=10)

    # Step 3: Decide text source for AI
    if transcript and len(transcript) > 200:
        text_for_ai = transcript
        source_type = "transcript"
        logger.info(f"Using transcript: {len(transcript)} chars")

    elif meta["description"] and len(meta["description"]) > 100:
        text_for_ai = meta["description"]
        source_type = "description"
        logger.warning("Transcript blocked. Using video description.")

    else:
        text_for_ai = (
            f"Bar exam lecture about {data.topic}. "
            f"Video: {meta['title']}. "
            f"This covers key {data.topic} concepts for the bar exam."
        )
        source_type = "title_only"
        logger.warning("Using title only as last resort")

    logger.info(f"Source: {source_type} | Length: {len(text_for_ai)}")

    # Step 4: Generate AI course content
    logger.info("Generating course content...")
    content = generate_course_content_groq(
        text_for_ai,
        meta["title"],
        data.topic
    )

    # Step 5: Save to Supabase
    logger.info("Saving module to Supabase...")
    try:
        module_result = supabase.table("course_modules").insert({
            "title":         meta["title"],
            "description":   content["summary"],
            "topic":         data.topic,
            "video_id":      video_id,
            "video_url":     f"https://youtube.com/watch?v={video_id}",
            "thumbnail_url": meta["thumbnail"],
            "ai_summary":    content["summary"],
            "ai_outline":    content["outline"],
            "order_index":   data.order_index,
            "is_published":  True
        }).execute()

        module_id = module_result.data[0]["id"]
        logger.info(f"Module saved: ID {module_id}")
    except Exception as e:
        raise HTTPException(
            status_code=500,
            detail=f"Failed to save module: {e}"
        )

    # Save transcript
    try:
        supabase.table("youtube_videos").upsert({
            "video_id":   video_id,
            "title":      meta["title"],
            "transcript": text_for_ai
        }).execute()
    except Exception as e:
        logger.error(f"Failed to save transcript: {e}")

    # Step 6: Embed for RAG
    logger.info("Embedding for RAG search...")
    chunks = split_text(text_for_ai)
    if len(chunks) > MAX_CHUNKS:
        chunks = chunks[:MAX_CHUNKS]

    try:
        embeddings = get_embeddings_batch(chunks)
        rows = [{
            "content":  chunk,
            "metadata": {
                "source":      f"https://youtube.com/watch?v={video_id}",
                "video_id":    video_id,
                "type":        "course_module",
                "module_id":   module_id,
                "topic":       data.topic,
                "source_type": source_type,
                "chunk_index": i
            },
            "embedding": embeddings[i]
        } for i, chunk in enumerate(chunks)]

        supabase.table("documents").insert(rows).execute()
        logger.info(f"Embedded {len(chunks)} chunks")
    except Exception as e:
        logger.error(f"Embedding failed: {e}")

    logger.info(f"=== Success: {meta['title']} ===")

    return {
        "message":         f"Course module created: {meta['title']}",
        "module_id":       module_id,
        "title":           meta["title"],
        "topic":           data.topic,
        "source_type":     source_type,
        "thumbnail":       meta["thumbnail"],
        "chunks_embedded": len(chunks)
    }


@app.post("/api/chat")
def chat_with_ai(data: ChatRequest):
    """Answers bar exam questions using RAG + AI"""

    # 1. Embed query
    try:
        query_vector = get_embeddings_batch([data.message])[0]
    except Exception as e:
        raise HTTPException(
            status_code=500,
            detail=f"Query embedding failed: {e}"
        )

    # 2. RAG search
    try:
        result = supabase.rpc("match_documents", {
            "query_embedding": query_vector,
            "match_threshold": RAG_THRESHOLD,
            "match_count":     RAG_MATCH_COUNT
        }).execute()
        context_chunks = [r["content"] for r in result.data]
    except Exception as e:
        logger.error(f"RAG search failed: {e}")
        context_chunks = []

    # 3. Build prompt
    context_text = sanitize_for_prompt(
        "\n---\n".join(context_chunks) if context_chunks
        else "No specific study material found."
    )

    system_prompt = f"""You are a professional encouraging Bar Exam prep coach
with deep knowledge of all MBE and MEE subjects including:
Constitutional Law, Contracts, Torts, Criminal Law, Civil Procedure,
Evidence, Real Property, Business Associations, Family Law, and Wills and Trusts.

Your role is to:
1. Answer bar exam questions accurately using your legal knowledge
2. Explain legal concepts step-by-step with clear legal reasoning
3. Reference relevant cases statutes and legal rules when applicable
4. Be encouraging and supportive to the student
5. If a student answers incorrectly explain why and give the correct answer

Do NOT include any promotional text advertisements or links.

=== STUDY MATERIAL CONTEXT ===
{context_text}"""

    # 4. Build messages
    messages = (
        [{"role": "system", "content": system_prompt}]
        + [{"role": m.role, "content": m.content} for m in data.history]
        + [{"role": "user", "content": data.message}]
    )

    # 5. Try Pollinations first
    try:
        res = requests.post(POLLINATIONS_URL, json={
            "messages": messages,
            "model":    "openai",
            "private":  True
        }, timeout=15)
        if res.status_code == 200 and res.text.strip():
            reply = clean_llm_response(res.text.strip())
            if reply:
                logger.info("Response via Pollinations")
                return {"reply": reply}
    except Exception as e:
        logger.warning(f"Pollinations failed: {e}")

    # 6. Groq fallback
    if groq_client:
        try:
            completion = groq_client.chat.completions.create(
                messages=messages,
                model=GROQ_MODEL,
                max_tokens=1024,
                temperature=0.7
            )
            reply = clean_llm_response(
                completion.choices[0].message.content.strip()
            )
            logger.info("Response via Groq")
            return {"reply": reply}
        except Exception as e:
            raise HTTPException(
                status_code=500,
                detail=f"Groq failed: {e}"
            )

    raise HTTPException(
        status_code=500,
        detail="All LLM services temporarily unavailable."
    )


