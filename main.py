# ---- Helpers ----

def clean_llm_response(text: str) -> str:
    if not text:
        return ""
    cleaned = text
    for pattern in AD_PATTERNS:
        cleaned = re.sub(pattern, "", cleaned, flags=re.IGNORECASE)
    cleaned = re.sub(r'\n{3,}', '\n\n', cleaned)
    return cleaned.strip()


def sanitize_for_prompt(text: str) -> str:
    text = text.replace("===", "---")
    return text[:8000]


def generate_hash_embedding(text: str) -> List[float]:
    embedding = np.zeros(EMBEDDING_DIM)
    for seed in range(EMBEDDING_DIM):
        hash_input = f"{seed}:{text}".encode("utf-8")
        hash_val = int(hashlib.md5(hash_input).hexdigest(), 16)
        embedding[seed] = (hash_val % 10000) / 10000.0 - 0.5
    norm = np.linalg.norm(embedding)
    if norm > 0:
        embedding = embedding / norm
    return embedding.tolist()


def get_embeddings_batch(texts: List[str]) -> List[List[float]]:
    clean_texts = [t.replace("\n", " ").strip() for t in texts]
    try:
        headers = {"Authorization": f"Bearer {HF_TOKEN}"} if HF_TOKEN else {}
        response = requests.post(
            HF_EMBED_URL,
            headers=headers,
            json={
                "inputs": clean_texts,
                "options": {"wait_for_model": True}
            },
            timeout=15
        )
        if response.status_code == 200:
            result = response.json()
            if isinstance(result, list) and len(result) > 0:
                logger.info("Embeddings via HuggingFace")
                return result
    except Exception as e:
        logger.warning(f"HuggingFace unavailable: {e}")

    logger.info("Using local hash embeddings")
    return [generate_hash_embedding(text) for text in clean_texts]


def split_text(
    text: str,
    chunk_size: int = CHUNK_SIZE,
    overlap: int = CHUNK_OVERLAP
) -> List[str]:
    if not text or not text.strip():
        return []
    if overlap >= chunk_size:
        raise ValueError("Overlap must be less than chunk_size")
    chunks, start, step = [], 0, chunk_size - overlap
    while start < len(text):
        chunks.append(text[start:start + chunk_size])
        start += step
    return chunks


# ---- Video Processing Helpers ----

def get_video_metadata(video_id: str) -> dict:
    """Get video info via YouTube Data API v3"""
    fallback = {
        "title":       f"YouTube Video {video_id}",
        "description": "",
        "thumbnail":   f"https://img.youtube.com/vi/{video_id}/maxresdefault.jpg",
        "channel":     ""
    }

    if not YOUTUBE_API_KEY:
        logger.warning("No YOUTUBE_API_KEY - using fallback metadata")
        return fallback

    try:
        url = (
            f"https://www.googleapis.com/youtube/v3/videos"
            f"?id={video_id}"
            f"&key={YOUTUBE_API_KEY}"
            f"&part=snippet"
        )
        res = requests.get(url, timeout=10)

        if not res.ok:
            logger.warning(f"YouTube API returned {res.status_code}")
            return fallback

        data = res.json()
        if not data.get("items"):
            logger.warning("Video not found in YouTube API")
            return fallback

        snippet    = data["items"][0]["snippet"]
        thumbnails = snippet.get("thumbnails", {})
        thumbnail  = (
            thumbnails.get("maxres",   {}).get("url") or
            thumbnails.get("standard", {}).get("url") or
            thumbnails.get("high",     {}).get("url") or
            fallback["thumbnail"]
        )

        logger.info(f"Got video metadata: {snippet['title']}")
        return {
            "title":       snippet.get("title",        fallback["title"]),
            "description": snippet.get("description",  "")[:1000],
            "thumbnail":   thumbnail,
            "channel":     snippet.get("channelTitle", "")
        }

    except Exception as e:
        logger.error(f"YouTube API metadata failed: {e}")
        return fallback


def get_transcript_with_timeout(
    video_id: str,
    timeout_seconds: int = 10
) -> Optional[str]:
    """
    Fetch transcript using thread with hard timeout.
    Works safely in FastAPI worker threads.
    Returns None if blocked or timed out.
    """
    result_container = {"transcript": None, "error": None}

    def fetch():
        try:
            transcript_list = YouTubeTranscriptApi.get_transcript(
                video_id,
                languages=["en", "en-US", "en-GB"]
            )
            text = " ".join(
                entry["text"] for entry in transcript_list
            )
            result_container["transcript"] = text
            logger.info(f"Transcript fetched: {len(text)} chars")
        except Exception as e:
            result_container["error"] = str(e)
            logger.warning(f"Transcript fetch error: {e}")

    thread = threading.Thread(target=fetch, daemon=True)
    thread.start()
    thread.join(timeout=timeout_seconds)

    if thread.is_alive():
        logger.warning(
            f"Transcript timed out after {timeout_seconds}s - using fallback"
        )
        return None

    return result_container.get("transcript")


def generate_course_content_groq(
    text: str,
    title: str,
    topic: str
) -> dict:
    """Generate AI summary and outline using Groq"""
    fallback = {
        "summary": (
            f"This lecture covers essential {topic} concepts "
            f"for the bar exam. Students will learn key legal "
            f"principles rules and their applications in exam scenarios."
        ),
        "outline": (
            f"• Core {topic} legal principles\n"
            f"• Key rules and standards\n"
            f"• Important cases and holdings\n"
            f"• Bar exam applications\n"
            f"• Practice tips"
        )
    }

    if not groq_client:
        return fallback

    try:
        prompt = (
            f"Topic: {topic}\n"
            f"Video: {title}\n"
            f"Content: {text[:6000]}\n\n"
            f"Format EXACTLY:\n\n"
            f"SUMMARY:\n"
            f"[3-4 sentences about what bar exam students will learn]\n\n"
            f"OUTLINE:\n"
            f"• [Key concept 1]\n"
            f"• [Key concept 2]\n"
            f"• [Key concept 3]\n"
            f"• [Key concept 4]\n"
            f"• [Key concept 5]"
        )

        response = groq_client.chat.completions.create(
            messages=[
                {
                    "role": "system",
                    "content": (
                        "You are a bar exam course creator. "
                        "Create structured educational content. "
                        "Be concise and focused on bar exam relevance."
                    )
                },
                {
                    "role": "user",
                    "content": prompt
                }
            ],
            model=GROQ_MODEL,
            max_tokens=500,
            temperature=0.5
        )

        content = response.choices[0].message.content

        summary_match = re.search(
            r'SUMMARY:\n([\s\S]+?)(?=\n\nOUTLINE:)', content
        )
        outline_match = re.search(
            r'OUTLINE:\n([\s\S]+?)$', content
        )

        return {
            "summary": (
                summary_match.group(1).strip()
                if summary_match else fallback["summary"]
            ),
            "outline": (
                outline_match.group(1).strip()
                if outline_match else fallback["outline"]
            )
        }

    except Exception as e:
        logger.error(f"Groq course generation failed: {e}")
        return fallback


